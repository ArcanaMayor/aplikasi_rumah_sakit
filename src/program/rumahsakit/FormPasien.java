    private void UbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UbahActionPerformed
        // TODO add your handling code here:
                 int i = tabelPasien.getSelectedRow();
        if (i == -1) {
// tidak ada baris terseleksi
            return;
        }
// ambil RM yang terseleksi
        String RM = (String) model.getValueAt(i, 0);
        String NAMA = inputNama.getText();
        java.util.Date TANGGAL_LAHIR = (java.util.Date) inputTanggalLahir.getValue();
        String JENIS_KELAMIN = inputJenisKelamin.getSelectedItem().toString();
        String ALAMAT = inputAlamat.getText();

        try {
            Connection c = KoneksiDatabase.getKoneksi();
            String sql = "UPDATE pasien SET NAMA = ?, TANGGAL_LAHIR = ?, JENIS_KELAMIN = ?,ALAMAT =  ? WHERE  RM=  ?";
            try (PreparedStatement p = c.prepareStatement(sql)) {
                p.setString(1, NAMA);
                p.setDate(2, new java.sql.Date(TANGGAL_LAHIR.getTime()));
                p.setString(3, JENIS_KELAMIN);
                p.setString(4, ALAMAT);
                p.setString(5, RM);
                p.executeUpdate();
                bersihkanForm();
                p.close();
            }
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        } finally {
            loadData();
        }

    }//GEN-LAST:event_UbahActionPerformed

