    private void TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahActionPerformed
        // TODO add your handling code here:
        String rm= inputRM.getText();
        String nama = inputNama.getText();
        java.util.Date tanggalLahir = (java.util.Date) inputTanggalLahir.getValue();
        String JenisKelamin = inputJenisKelamin.getSelectedItem().toString();
        String alamat = inputAlamat.getText();

        try {
            Connection c = KoneksiDatabase.getKoneksi();
            String sql = "INSERT INTO pasien VALUES (?, ?, ?, ?, ?)";
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, rm);
            p.setString(2, nama);
            p.setDate(3, new java.sql.Date(tanggalLahir.getTime()));
            p.setString(4, JenisKelamin);
            p.setString(5, alamat);
            p.executeUpdate();
            bersihkanForm();
            p.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        } finally {
            loadData();
        }

    }//GEN-LAST:event_TambahActionPerformed

